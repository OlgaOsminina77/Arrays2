    private void jButton_resultActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_resultActionPerformed
        String input = jTextField_inputHeitMatrixPart3.getText();
        ArrayList<Integer> sequencesDigits = new ArrayList<Integer>();
        int countDigits = 0;

        // a123
        for (int i = 0; i < input.length(); i++) {
            char inputChar = input.charAt(i); //проходимся по символом 
            int charCode = (int) inputChar; // преобразует символ по таблице
            if (charCode >= 48 & charCode <= 57) { // 48 <= код символа <= 57
                countDigits++;
            } else {
                if (countDigits != 0) {
                    sequencesDigits.add(countDigits);
                }
                if (i != input.length() - 1) {
                    countDigits = 0;
                }
            }
        }
        if (countDigits != 0) {
            sequencesDigits.add(countDigits);
        }

        int maxDigitSequence = Integer.MIN_VALUE;
        for (int i = 0; i < sequencesDigits.size(); i++) {
            if (maxDigitSequence < sequencesDigits.get(i)) {
                maxDigitSequence = sequencesDigits.get(i);
            }
        }

        jLabel_Result.setText("Результат: " + Integer.toString(maxDigitSequence));
    }//GEN-LAST:event_jButton_resultActionPerformed

    private void jButton_calculatePart3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_calculatePart3ActionPerformed
        String input = (jTextField_inputWidhtMatrixPart3.getText()); // задана переменная
        String output = ""; // Переменная для вывода
        int halfLenght = input.length() / 2; // Получаем половину длины
        String leftPart = input.substring(0, halfLenght); // Левая часть строки

        if (input.length() % 2 == 1) { // проверка на четность
            String rightPart = input.substring(halfLenght + 1, input.length()); // Правая часть за исключением среднего элемента
            String middleElement = String.valueOf(input.charAt(halfLenght)); // Средний символ
            String reversedLeftPart = reverseString(leftPart); // Перевернутая левая часть
            String reversedRightPart = reverseString(rightPart); // Перевернутая правая часть

            output += reversedRightPart + middleElement + reversedLeftPart; // Объединяем все в одну строку
        } else {
            String rightPart = input.substring(halfLenght, input.length()); // Правая часть
            String reversedLeftPart = reverseString(leftPart); // Перевернутая левая часть
            String reversedRightPart = reverseString(rightPart); // Перевернутая правая часть

            output += reversedRightPart + reversedLeftPart; // Объединяем все в одну строку
        }

        jLabel_resultPart3.setText(output); // Вывод
    }//GEN-LAST:event_jButton_calculatePart3ActionPerformed

