class Worker {

    private String firstName; //имя
    private String lastName; //фамилия
    private String middleName; //отчесво
    private int numberdaysworked;
    private int salaryamount;

    public Worker(String firstName, String lastName, String middleName, int numberdaysworked, int salaryamount) {
        this.firstName = firstName;
        this.lastName = lastName;
        this.middleName = middleName;
        this.numberdaysworked = numberdaysworked;
        this.salaryamount = salaryamount;
    }

    public int getNumberdaysworked() {
        return numberdaysworked;
    }

    public int getSalaryamount() {
        return salaryamount;
    }

    public String getFirstName() {
        return firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public String getMiddleName() {
        return middleName;
    }

    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    public void setMiddleName(String middleName) {
        this.middleName = middleName;
    }

    public void setNumberdaysworked(int numberdaysworked) {
        this.numberdaysworked = numberdaysworked;
    }

    public void setSalaryamount(int salaryamount) {
        this.salaryamount = salaryamount;
    }

}
